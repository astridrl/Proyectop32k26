    private void btnbuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnbuscarActionPerformed
                                         
    String idABuscar = txtbuscado.getText().trim();
    if (idABuscar.isEmpty()) {
        llenarTabla();
        return;
    }

    DefaultTableModel modelo = (DefaultTableModel) tablaAPU.getModel();
    modelo.setRowCount(0);
    
    List<Controlador.AsignacionPerfilUsuario> lista = dao.select();
    for (Controlador.AsignacionPerfilUsuario asig : lista) {
        
        if (String.valueOf(asig.getUsucodigo()).equals(idABuscar)) {
            modelo.addRow(new Object[]{asig.getUsucodigo(), asig.getPercodigo()});
        }
    }
    
        
    }//GEN-LAST:event_btnbuscarActionPerformed

    private void btnlimpiarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnlimpiarActionPerformed

        cbxusuario.setSelectedIndex(0);
        cbxperfil.setSelectedIndex(0);
        txtbuscado.setText("");

        llenarTabla();
    }//GEN-LAST:event_btnlimpiarActionPerformed

    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed
int filaSeleccionada = tablaAPU.getSelectedRow();
    
    if (filaSeleccionada >= 0) {
       
        int idUsu = (int) tablaAPU.getValueAt(filaSeleccionada, 0);
        int idPer = (int) tablaAPU.getValueAt(filaSeleccionada, 1);

        Controlador.AsignacionPerfilUsuario asigAEliminar = new Controlador.AsignacionPerfilUsuario(idUsu, idPer);

        
        if (dao.delete(asigAEliminar) > 0) {
            JOptionPane.showMessageDialog(null, "Asignación eliminada correctamente");
            llenarTabla();
        }
    } else {
        JOptionPane.showMessageDialog(null, "Por favor, selecciona una fila de la tabla primero");
    }
        
    }//GEN-LAST:event_btneliminarActionPerformed

    private void btnregistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnregistrarActionPerformed

        try{
        int idUsu = Integer.parseInt(cbxusuario.getSelectedItem().toString().split(" - ")[0]);
        int idPer = Integer.parseInt(cbxperfil.getSelectedItem().toString().split(" - ")[0]);

    
        Controlador.AsignacionPerfilUsuario nuevaAsig = new Controlador.AsignacionPerfilUsuario(idUsu, idPer);

        
        if (dao.insert(nuevaAsig) > 0) {
            JOptionPane.showMessageDialog(null, "Asignación registrada con éxito");
            llenarTabla(); 
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Error al registrar: " + e.getMessage());
    }



    }//GEN-LAST:event_btnregistrarActionPerformed



