public void llenarTabla() {
    DefaultTableModel modelo = (DefaultTableModel) tablaAPU.getModel();
    modelo.setRowCount(0); 
    
    List<Controlador.AsignacionPerfilUsuario> lista = dao.select();
    
    for (Controlador.AsignacionPerfilUsuario asig : lista) {
       
        Object[] fila = {asig.getUsucodigo(), asig.getPercodigo()};
        modelo.addRow(fila);
    }
}

public void cargarCombos() {
    try (Connection conn = Modelo.Conexion.getConnection()) {
        // Cargar Usuarios
        PreparedStatement stmtU = conn.prepareStatement("SELECT Usucodigo, Usunombre FROM usuario");
        ResultSet rsU = stmtU.executeQuery();
        cbxusuario.removeAllItems();
        while (rsU.next()) {
            cbxusuario.addItem(rsU.getInt("Usucodigo") + " - " + rsU.getString("Usunombre"));
        }

        // Cargar Perfiles
        PreparedStatement stmtP = conn.prepareStatement("SELECT Percodigo, Pernombre FROM perfiles");
        ResultSet rsP = stmtP.executeQuery();
        cbxperfil.removeAllItems();
        while (rsP.next()) {
            cbxperfil.addItem(rsP.getInt("Percodigo") + " - " + rsP.getString("Pernombre"));
        }
    } catch (SQLException ex) {
        ex.printStackTrace(System.out);
    }
}
    
    

